void build(int node, int start, int end)
{
    if(start == end)
    {
        // Leaf node will have a single element
        tree1[node] = A[start];
        tree2[node]=A[start];
    }
    else
    {
        int mid = (start + end) / 2;
        // Recurse on the left child
        build(2*node, start, mid);
        // Recurse on the right child
        build(2*node+1, mid+1, end);
        // Internal node will have the sum of both of its children
        tree1[node] = f1(tree1[2*node],tree1[2*node+1]);
        tree2[node] = f2(tree2[2*node],tree2[2*node+1]);
    }
}



pll query(int node, int start, int end, int l, int r)
{
    if(r < start or end < l)
    {
        // range represented by a node is completely outside the given range
        return {0,0};
    }
    if(l <= start and end <= r)
    {
        // range represented by a node is completely inside the given range
        return {tree1[node],tree2[node]};
    }
    // range represented by a node is partially inside and partially outside the given range
    int mid = (start + end) / 2;
    pll p1 = query(2*node, start, mid, l, r);
    pll p2 = query(2*node+1, mid+1, end, l, r);
    p1.first=p1.first+p2.first;
    p1.second=p1.second^p2.second;
    return p1;
}
